package com.jcool.dev.travel.bean;

public class HomeIconBean {
    private String name;
    private int iconId;
    private int width;
    private int  bg;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIconId() {
        return iconId;
    }

    public void setIconId(int iconId) {
        this.iconId = iconId;
    }

    public int getWidth() {
        return width;
    }

    public int getBg() {
        return bg;
    }

    public void setBg(int bg) {
        this.bg = bg;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
